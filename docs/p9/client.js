function iniciaMapa() {
    var styledMapType = new google.maps.StyledMapType(mapStyle, {
        name: "Mapa Oscuro",
      });

  var styledMapTypeGreen = new google.maps.StyledMapType(mapStyleGreen, {
    name: "Mapa Verde",
  });

  var propiedades = {
    center: {
      lat: 21.152639,
      lng: -101.711598,
    },
    zoom: 14,
    mapTypeControlOptions: {
      mapTypeIds: ["roadmap", "satellite", "hybrid", "terrain", "style_map"],
    },
  };

  var mapa = document.getElementById("map");

  var map = new google.maps.Map(mapa, propiedades);

  map.mapTypes.set("style_map", styledMapType);
  map.mapTypes.set("style_map", styledMapTypeGreen);
  map.setMapTypeId("style_map");
  
}
